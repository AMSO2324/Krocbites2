    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

// Variables para cálculos
int Sue = 0, Bon = 0, Des = 0, TO = 0;

// Determinación del sueldo según ComboBox
switch (this.jComboBox1.getSelectedIndex()) {
    case 0: // Cajero
        switch (this.jComboBox2.getSelectedIndex()) {
            case 0:
                Sue = 360;
                break;
            case 1:
                Sue = 370;
                break;
        }
        break;
    case 1: // Bodeguero
        switch (this.jComboBox2.getSelectedIndex()) {
            case 0:
                Sue = 420;
                break;
            case 1:
                Sue = 550;
                break;
        }
        break;
    case 2: // Supervisor
        switch (this.jComboBox2.getSelectedIndex()) {
            case 0:
                Sue = 570;
                break;
            case 1:
                Sue = 660;
                break;
        }
        break;
}

// Cálculo de la bonificación
switch (this.jComboBox3.getSelectedIndex()) {
    case 0:
        Bon = Sue * 5 / 100;
        break;
    case 1:
        Bon = Sue * 10 / 100;
        break;
    case 2:
        Bon = Sue * 25 / 100;
        break;
}

// Cálculo de los descuentos
switch (this.jComboBox2.getSelectedIndex()) {
    case 0:
        Des = Sue * 12 / 100;
        break;
    case 1:
        Des = Sue * 8 / 100;
        break;
}

// Cálculo del total
TO = Bon + Sue - Des;

// Asignar valores a los JTextField
this.Tsueldo.setText("" + Sue);
this.Tbonificacion.setText("" + Bon);
this.Tdescuento.setText("" + Des);
this.Ttota.setText("" + TO);
       
        
    }//GEN-LAST:event_jButton1ActionPerformed

